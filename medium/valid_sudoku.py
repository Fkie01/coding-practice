from tabnanny import check
from typing import DefaultDict


def isValidSudoku(board: list[list[str]]) -> bool:
    row = DefaultDict(set)
    col = DefaultDict(set)
    sqrt = DefaultDict(set)

    for r in range(len(board)):
        for c in range(len(board)):
            if board[r][c] == ".":
                continue
            if (
                board[r][c] in row[r]
                or board[r][c] in col[c]
                or board[r][c] in sqrt[(r // 3, c // 3)]
            ):
                return False

            row[r].add(board[r][c])
            col[c].add(board[r][c])
            sqrt[(r // 3, c // 3)].add(board[r][c])
    return True


